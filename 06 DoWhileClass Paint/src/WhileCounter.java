    public void paint( Graphics g ) {
        int counter = 1;            //Inicializacion del contador
        
        while ( counter <= 10 ) {   //Condicion de repeticion 
            g.drawLine( 10, 10, 250, counter * 10 ); 
            counter =counter +1; //incremento
        }
        
    
                        
}
